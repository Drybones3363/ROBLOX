local function Add(tbl)
	local ds = game:GetService("DataStoreService"):GetDataStore("Data-Released")
	local dstbl = ds:GetAsync("Customization") or {Hats={},Packages={},Faces={},Skins={},Classes={},Jackets={},Pants={}}
	for i,k in pairs (tbl) do
		--if i ~= "Skins" then
			for e,r in pairs (k) do
				local function Legal()
					local t = dstbl[i]
					for q,w in pairs (t) do
						if w[1] == r[1] then
							return false
						end
					end
					return true
				end
				if Legal() == true then
					table.insert(dstbl[i],r)
				end
			end
		--end
	end
	ds:SetAsync("Customization",dstbl)
	print("Saved Extras :)")
end
Add(addtbl)
